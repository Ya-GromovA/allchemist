import type { ChemistryTopicContent } from "@app/chemistry/chemistryContentSchema";

export type CatalogTopic = ChemistryTopicContent;

function makeTopic(input: {
  id: string;
  grade: string;
  book: string;
  author: string;
  title: string;
  summary: string;
  content: string;
  explanation?: string;
  examples?: string[];
  keyTerms?: string[];
  formulaBlock?: string[];
  commonMistakes?: string[];
  miniCheck?: string[];
  visuals?: string[];
  parentNote?: string;
  teacherNote?: string;
  examHints?: Array<{ exam: "ОГЭ" | "ЕГЭ" | "МЦКО"; prompt: string }>;
}): CatalogTopic {
  const keyTerms = input.keyTerms ?? [];
  const formulaBlock = input.formulaBlock ?? [];
  const commonMistakes = input.commonMistakes ?? ["Не ограничивайся заучиванием, связывай определение с примером из темы."];
  const miniCheck = input.miniCheck ?? ["Сможешь ли ты объяснить тему своими словами за 30 секунд?"];
  const explanation = input.explanation ?? `Тема раскрывается через причинно-следственные связи и примеры, а не только через определение. ${input.content}`;
  const examples = input.examples ?? ["Объясни тему на одном бытовом примере.", "Свяжи тему с одной реальной задачей или реакцией."];
  return {
    id: input.id,
    grade: input.grade,
    title: input.title,
    summary: input.summary,
    bookLine: {
      id: `${input.grade}-${input.author}-${input.book}`.toLowerCase().replace(/[^a-zа-я0-9]+/gi, "-"),
      book: input.book,
      author: input.author,
    },
    theory: {
      gradeMode: {
        short: input.summary,
        full: input.content,
        explanation,
        examples,
        keyTerms,
        formulaBlock,
        commonMistakes,
        miniCheck,
      },
      bookLineMode: {
        short: `Тема по линии ${input.author}: ${input.summary}`,
        full: `В учебной линии ${input.author} эта тема подается через последовательность: ${input.summary.toLowerCase()} ${input.content}`,
        explanation: `В логике учебной линии ${input.author} важно не просто выучить формулировку, а понять, как тема связана с предыдущими разделами. ${explanation}`,
        examples,
        keyTerms,
        formulaBlock,
        commonMistakes,
        miniCheck,
      },
    },
    visuals: input.visuals ?? ["Схема процесса", "Карточка терминов", "Мини-анимация ключевого этапа"],
    examHints: input.examHints ?? [
      { exam: "ОГЭ", prompt: "Сверь тему с типовыми заданиями базового уровня." },
      { exam: "МЦКО", prompt: "Используй тему для короткой проверочной работы по разделу." },
    ],
    parentNote: input.parentNote ?? "Попросите ученика пересказать тему простыми словами и назвать один бытовой пример.",
    teacherNote: input.teacherNote ?? "Используйте тему как короткий explain-first блок перед задачами и демонстрацией.",
  };
}

